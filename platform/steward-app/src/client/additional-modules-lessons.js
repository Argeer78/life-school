/**
 * @param {{
 *   moduleCode: string,
 *   slug: string,
 *   moduleTitle: string,
 *   lessons: readonly {
 *     title: string,
 *     estimatedDuration: string,
 *     introduction: string,
 *     why: string,
 *     concept: string,
 *     quote: string,
 *     distinctionOne: string,
 *     distinctionTwo: string,
 *     exampleParagraph: string,
 *     exerciseInstruction: string,
 *     fields: readonly {
 *       id: string,
 *       label: string,
 *       placeholder: string
 *     }[],
 *     reflectionPrompt: string,
 *     reflectionPlaceholder: string,
 *     practiceDefaultPrompt: string,
 *     practiceHandoff: string,
 *     completionCriteria: string
 *   }[]
 * }} moduleSpec
 */
function buildModuleLessons(moduleSpec) {
  return Object.freeze(
    moduleSpec.lessons.map((lesson, index) => {
      const lessonNumber = index + 1;
      const route =
        lessonNumber === 1
          ? `/courses/${moduleSpec.slug}`
          : `/courses/${moduleSpec.slug}/lesson-${lessonNumber}`;
      const nextLesson =
        lessonNumber === moduleSpec.lessons.length
          ? null
          : {
              href: `/courses/${moduleSpec.slug}/lesson-${lessonNumber + 1}`,
              label: `Continue to Lesson ${lessonNumber + 1}`,
            };

      return defineLesson({
        id: `${moduleSpec.moduleCode}-LESSON-${lessonNumber}`,
        route,
        moduleTitle: moduleSpec.moduleTitle,
        lessonNumber,
        totalLessons: moduleSpec.lessons.length,
        title: lesson.title,
        estimatedDuration: lesson.estimatedDuration,
        introduction: lesson.introduction,
        whyThisMatters: {
          paragraphs: [lesson.why],
        },
        concept: {
          paragraphs: [lesson.concept],
        },
        example: {
          quote: lesson.quote,
          distinctions: [
            { term: "Observation", description: lesson.distinctionOne },
            { term: "Examination", description: lesson.distinctionTwo },
          ],
          paragraphs: [lesson.exampleParagraph],
        },
        exercise: {
          instructions: [lesson.exerciseInstruction],
          fields: lesson.fields.map((field) => ({
            id: field.id,
            label: field.label,
            placeholder: field.placeholder,
            rows: 3,
            maxLength: 1500,
          })),
        },
        reflection: {
          prompt: lesson.reflectionPrompt,
          placeholder: lesson.reflectionPlaceholder,
        },
        practicePromptTemplate: {
          instructions:
            "Use the starter prompt or continue with your exercise. Steward responds through the same learner-safe production path.",
          defaultPrompt: lesson.practiceDefaultPrompt,
          handoff: lesson.practiceHandoff,
        },
        completionCriteria: lesson.completionCriteria,
        nextLesson,
      });
    }),
  );
}

